//! 用 `sha2` 计算哈希摘要，并用 `hex` 把摘要编码为十六进制字符串。
//!
//! 哈希（hash）把任意长度的数据映射成固定长度的“摘要”（digest）。
//! 同样的输入永远得到同样的输出，且输入只要变一个比特，输出就会面目全非。
//! 这一性质让哈希非常适合做**完整性校验 / 校验和（checksum）**：
//! 下载文件后比对哈希，就能确认内容没被损坏或篡改。
//!
//! 本章覆盖：
//! - 用 `Sha256` / `Sha512` 配合 `Digest` trait 计算摘要；
//! - 用 `hex::encode` 把二进制摘要转成小写十六进制字符串；
//! - 写一个 `matches` 校验函数，对比数据的哈希与给定的期望值。
//!
//! 重要提醒：
//! - 这里的哈希用于**完整性校验**，**不是**用来存密码的。
//!   存密码请用专门的口令哈希算法（如 `argon2` / `bcrypt`）。
//! - 本章的校验对比**不是常数时间**的，仅作教学，不适合对抗计时攻击的场景。
//!
//! 代码按职责拆分为两个模块：
//! - [`hashing`]：`sha256_hex` / `sha256_str` / `sha512_hex` 等计算函数；
//! - [`verify`]：`matches` 校验函数。

pub mod hashing;
pub mod verify;

pub use hashing::{sha256_hex, sha256_str, sha512_hex};
pub use verify::matches;
